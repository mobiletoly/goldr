package wiring

import (
	"bytes"
	"errors"
	"fmt"
	"go/format"
	"go/token"
	"slices"
	"strconv"
	"strings"

	"github.com/mobiletoly/goldr/internal/routing"
)

const URLGeneratedFileName = "goldr_gen.go"

var ErrAmbiguousURLHelper = errors.New("ambiguous URL helper")

type GenerateURLOptions struct {
	PackageName string
}

type urlHelperParam struct {
	name  string
	field string
	arg   string
}

type urlHelperNode struct {
	apiName         string
	typeName        string
	segment         string
	isPath          bool
	isDynamic       bool
	param           urlHelperParam
	params          []urlHelperParam
	segments        []string
	staticChildren  []*urlHelperNode
	dynamicChildren []*urlHelperNode
	staticByName    map[string]*urlHelperNode
	dynamicByName   map[string]*urlHelperNode
}

func GenerateURLHelpers(manifest routing.Manifest, options GenerateURLOptions) ([]byte, error) {
	if !isPackageName(options.PackageName) {
		return nil, fmt.Errorf("%w %q: must be a lowercase Go-safe identifier", ErrInvalidPackageName, options.PackageName)
	}

	routes, err := runtimeRoutes(manifest)
	if err != nil {
		return nil, err
	}
	root, err := buildURLHelperTree(runtimePaths(routes))
	if err != nil {
		return nil, err
	}

	var buffer bytes.Buffer
	fmt.Fprintf(&buffer, "// Code generated by goldr; DO NOT EDIT.\n\n")
	fmt.Fprintf(&buffer, "package %s\n\n", options.PackageName)
	writeURLImports(&buffer, root)
	writeURLVars(&buffer, root)
	writeURLTypes(&buffer, root)
	writeURLConstructors(&buffer, root)
	writeURLRootDynamicFunctions(&buffer, root)
	writeURLDynamicMethods(&buffer, root)
	writeURLPathMethods(&buffer, root)

	source, err := format.Source(buffer.Bytes())
	if err != nil {
		return nil, fmt.Errorf("format generated URL helpers: %w", err)
	}
	return source, nil
}

func buildURLHelperTree(paths []runtimePath) (*urlHelperNode, error) {
	root := &urlHelperNode{
		apiName:       "Root",
		typeName:      "rootRoute",
		staticByName:  make(map[string]*urlHelperNode),
		dynamicByName: make(map[string]*urlHelperNode),
	}

	for _, routePath := range paths {
		if routePath.route == "/" {
			root.isPath = true
			continue
		}

		node := root
		for _, segment := range routePath.segments {
			var err error
			if paramName, ok := paramSegmentName(segment); ok {
				node, err = urlDynamicChild(node, segment, paramName)
			} else {
				node, err = urlStaticChild(node, segment)
			}
			if err != nil {
				return nil, err
			}
		}
		node.isPath = true
	}

	sortURLHelperTree(root)
	return root, nil
}

func urlStaticChild(parent *urlHelperNode, segment string) (*urlHelperNode, error) {
	apiName := exportedSegmentName(segment)
	if parent.typeName == "rootRoute" && apiName == "Root" {
		return nil, fmt.Errorf("%w %q: top-level route segment collides with reserved Root helper", ErrAmbiguousURLHelper, segment)
	}
	if apiName == "Path" {
		return nil, fmt.Errorf("%w %q: route segment collides with Path method", ErrAmbiguousURLHelper, segment)
	}
	if child, ok := parent.staticByName[apiName]; ok {
		if child.segment == segment {
			return child, nil
		}
		return nil, fmt.Errorf("%w %q and %q both map to %s", ErrAmbiguousURLHelper, child.segment, segment, apiName)
	}
	if child, ok := parent.dynamicByName[apiName]; ok {
		return nil, fmt.Errorf("%w %q collides with dynamic helper %s for %q", ErrAmbiguousURLHelper, segment, apiName, child.segment)
	}

	child := &urlHelperNode{
		apiName:       apiName,
		typeName:      routeTypeName(append(urlAPIChain(parent), apiName)),
		segment:       segment,
		params:        slices.Clone(parent.params),
		segments:      append(slices.Clone(parent.segments), segment),
		staticByName:  make(map[string]*urlHelperNode),
		dynamicByName: make(map[string]*urlHelperNode),
	}
	parent.staticByName[apiName] = child
	parent.staticChildren = append(parent.staticChildren, child)
	return child, nil
}

func urlDynamicChild(parent *urlHelperNode, segment, paramName string) (*urlHelperNode, error) {
	apiName := "By" + exportedSegmentName(paramName)
	if apiName == "Path" {
		return nil, fmt.Errorf("%w %q: dynamic route segment collides with Path method", ErrAmbiguousURLHelper, segment)
	}
	if child, ok := parent.dynamicByName[apiName]; ok {
		if child.segment == segment {
			return child, nil
		}
		return nil, fmt.Errorf("%w %q and %q both map to %s", ErrAmbiguousURLHelper, child.segment, segment, apiName)
	}
	if child, ok := parent.staticByName[apiName]; ok {
		return nil, fmt.Errorf("%w %q collides with static helper %s for %q", ErrAmbiguousURLHelper, segment, apiName, child.segment)
	}

	param := urlHelperParam{
		name:  paramName,
		field: unexportedIdentifier(paramName),
		arg:   unexportedIdentifier(paramName),
	}
	for _, existing := range parent.params {
		if existing.field == param.field {
			return nil, fmt.Errorf("%w %q: dynamic param name %q is already used on this helper path", ErrAmbiguousURLHelper, segment, paramName)
		}
	}

	child := &urlHelperNode{
		apiName:       apiName,
		typeName:      routeTypeName(append(urlAPIChain(parent), apiName)),
		segment:       segment,
		isDynamic:     true,
		param:         param,
		params:        append(slices.Clone(parent.params), param),
		segments:      append(slices.Clone(parent.segments), segment),
		staticByName:  make(map[string]*urlHelperNode),
		dynamicByName: make(map[string]*urlHelperNode),
	}
	parent.dynamicByName[apiName] = child
	parent.dynamicChildren = append(parent.dynamicChildren, child)
	return child, nil
}

func sortURLHelperTree(node *urlHelperNode) {
	slices.SortFunc(node.staticChildren, func(a, b *urlHelperNode) int {
		return strings.Compare(a.apiName, b.apiName)
	})
	slices.SortFunc(node.dynamicChildren, func(a, b *urlHelperNode) int {
		return strings.Compare(a.apiName, b.apiName)
	})
	for _, child := range node.staticChildren {
		sortURLHelperTree(child)
	}
	for _, child := range node.dynamicChildren {
		sortURLHelperTree(child)
	}
}

func writeURLImports(buffer *bytes.Buffer, root *urlHelperNode) {
	if !urlTreeHasDynamic(root) {
		return
	}
	buffer.WriteString("import \"net/url\"\n\n")
}

func writeURLVars(buffer *bytes.Buffer, root *urlHelperNode) {
	if root.isPath {
		fmt.Fprintf(buffer, "var Root = new%s()\n", exportedTypeName(root.typeName))
	}
	for _, child := range root.staticChildren {
		fmt.Fprintf(buffer, "var %s = new%s()\n", child.apiName, exportedTypeName(child.typeName))
	}
	if root.isPath || len(root.staticChildren) > 0 {
		buffer.WriteString("\n")
	}
}

func writeURLTypes(buffer *bytes.Buffer, root *urlHelperNode) {
	for _, node := range urlHelperNodes(root) {
		staticChildren := urlStaticFields(node)
		fmt.Fprintf(buffer, "type %s struct", node.typeName)
		if len(node.params) == 0 && len(staticChildren) == 0 {
			buffer.WriteString("{}\n\n")
			continue
		}
		buffer.WriteString(" {\n")
		for _, param := range node.params {
			fmt.Fprintf(buffer, "\t%s string\n", param.field)
		}
		for _, child := range staticChildren {
			fmt.Fprintf(buffer, "\t%s %s\n", child.apiName, child.typeName)
		}
		buffer.WriteString("}\n\n")
	}
}

func writeURLConstructors(buffer *bytes.Buffer, root *urlHelperNode) {
	for _, node := range urlHelperNodes(root) {
		staticChildren := urlStaticFields(node)
		fmt.Fprintf(buffer, "func new%s(", exportedTypeName(node.typeName))
		writeURLConstructorParams(buffer, node.params)
		fmt.Fprintf(buffer, ") %s {\n", node.typeName)
		fmt.Fprintf(buffer, "\treturn %s", node.typeName)
		if len(node.params) == 0 && len(staticChildren) == 0 {
			buffer.WriteString("{}\n")
			buffer.WriteString("}\n\n")
			continue
		}
		buffer.WriteString("{\n")
		for _, param := range node.params {
			fmt.Fprintf(buffer, "\t\t%s: %s,\n", param.field, param.field)
		}
		for _, child := range staticChildren {
			fmt.Fprintf(buffer, "\t\t%s: new%s(", child.apiName, exportedTypeName(child.typeName))
			writeURLConstructorArgs(buffer, child.params)
			buffer.WriteString("),\n")
		}
		buffer.WriteString("\t}\n")
		buffer.WriteString("}\n\n")
	}
}

func urlStaticFields(node *urlHelperNode) []*urlHelperNode {
	if node.typeName == "rootRoute" {
		return nil
	}
	return node.staticChildren
}

func writeURLDynamicMethods(buffer *bytes.Buffer, root *urlHelperNode) {
	for _, node := range urlHelperNodes(root) {
		if node.typeName == "rootRoute" {
			continue
		}
		for _, child := range node.dynamicChildren {
			fmt.Fprintf(buffer, "func (r %s) %s(%s string) %s {\n", node.typeName, child.apiName, child.param.arg, child.typeName)
			escapedName := "escaped" + exportedSegmentName(child.param.name)
			fmt.Fprintf(buffer, "\t%s := url.PathEscape(%s)\n", escapedName, child.param.arg)
			fmt.Fprintf(buffer, "\treturn new%s(", exportedTypeName(child.typeName))
			writeURLDynamicConstructorArgs(buffer, node, child, escapedName)
			buffer.WriteString(")\n")
			buffer.WriteString("}\n\n")
		}
	}
}

func writeURLRootDynamicFunctions(buffer *bytes.Buffer, root *urlHelperNode) {
	for _, child := range root.dynamicChildren {
		fmt.Fprintf(buffer, "func %s(%s string) %s {\n", child.apiName, child.param.arg, child.typeName)
		escapedName := "escaped" + exportedSegmentName(child.param.name)
		fmt.Fprintf(buffer, "\t%s := url.PathEscape(%s)\n", escapedName, child.param.arg)
		fmt.Fprintf(buffer, "\treturn new%s(%s)\n", exportedTypeName(child.typeName), escapedName)
		buffer.WriteString("}\n\n")
	}
}

func writeURLPathMethods(buffer *bytes.Buffer, root *urlHelperNode) {
	for _, node := range urlHelperNodes(root) {
		if !node.isPath {
			continue
		}
		fmt.Fprintf(buffer, "func (r %s) Path() string {\n", node.typeName)
		fmt.Fprintf(buffer, "\treturn %s\n", urlPathExpression(node))
		buffer.WriteString("}\n\n")
	}
}

func writeURLConstructorParams(buffer *bytes.Buffer, params []urlHelperParam) {
	for index, param := range params {
		if index > 0 {
			buffer.WriteString(", ")
		}
		fmt.Fprintf(buffer, "%s string", param.field)
	}
}

func writeURLConstructorArgs(buffer *bytes.Buffer, params []urlHelperParam) {
	for index, param := range params {
		if index > 0 {
			buffer.WriteString(", ")
		}
		buffer.WriteString(param.field)
	}
}

func writeURLDynamicConstructorArgs(buffer *bytes.Buffer, parent, child *urlHelperNode, escapedName string) {
	parentParams := make(map[string]bool, len(parent.params))
	for _, param := range parent.params {
		parentParams[param.field] = true
	}
	for index, param := range child.params {
		if index > 0 {
			buffer.WriteString(", ")
		}
		if parentParams[param.field] {
			buffer.WriteString("r.")
		} else if param.field == child.param.field {
			buffer.WriteString(escapedName)
			continue
		}
		buffer.WriteString(param.field)
	}
}

func urlPathExpression(node *urlHelperNode) string {
	if len(node.segments) == 0 {
		return strconv.Quote("/")
	}

	var parts []string
	paramFields := make(map[string]string, len(node.params))
	for _, param := range node.params {
		paramFields[param.name] = param.field
	}
	for _, segment := range node.segments {
		parts = append(parts, strconv.Quote("/"))
		if paramName, ok := paramSegmentName(segment); ok {
			parts = append(parts, "r."+paramFields[paramName])
			continue
		}
		parts = append(parts, strconv.Quote(segment))
	}
	return strings.Join(parts, " + ")
}

func urlHelperNodes(root *urlHelperNode) []*urlHelperNode {
	var nodes []*urlHelperNode
	if root.isPath {
		nodes = append(nodes, root)
	}
	for _, child := range root.staticChildren {
		nodes = append(nodes, urlHelperNodesIncluding(child)...)
	}
	for _, child := range root.dynamicChildren {
		nodes = append(nodes, urlHelperNodesIncluding(child)...)
	}
	return nodes
}

func urlHelperNodesIncluding(node *urlHelperNode) []*urlHelperNode {
	nodes := []*urlHelperNode{node}
	for _, child := range node.staticChildren {
		nodes = append(nodes, urlHelperNodesIncluding(child)...)
	}
	for _, child := range node.dynamicChildren {
		nodes = append(nodes, urlHelperNodesIncluding(child)...)
	}
	return nodes
}

func urlTreeHasDynamic(node *urlHelperNode) bool {
	if len(node.dynamicChildren) > 0 {
		return true
	}
	return slices.ContainsFunc(node.staticChildren, urlTreeHasDynamic)
}

func urlAPIChain(node *urlHelperNode) []string {
	var chain []string
	if node.typeName == "rootRoute" {
		return chain
	}
	base := strings.TrimSuffix(node.typeName, "Route")
	for _, word := range splitIdentifierWords(base) {
		chain = append(chain, exportedWord(word))
	}
	return chain
}

func routeTypeName(parts []string) string {
	if len(parts) == 0 {
		return "rootRoute"
	}
	var builder strings.Builder
	for _, part := range parts {
		builder.WriteString(part)
	}
	return unexportedFirst(builder.String()) + "Route"
}

func exportedTypeName(typeName string) string {
	return exportedWord(strings.TrimSuffix(typeName, "Route")) + "Route"
}

func exportedSegmentName(segment string) string {
	words := segmentWords(segment)
	var builder strings.Builder
	for _, word := range words {
		builder.WriteString(exportedWord(word))
	}
	return builder.String()
}

func unexportedIdentifier(segment string) string {
	words := segmentWords(segment)
	if len(words) == 0 {
		return "value"
	}

	var builder strings.Builder
	for index, word := range words {
		if index == 0 {
			builder.WriteString(strings.ToLower(word))
			continue
		}
		builder.WriteString(exportedWord(word))
	}
	result := builder.String()
	if token.Lookup(result).IsKeyword() {
		return result + "Value"
	}
	return result
}

func segmentWords(segment string) []string {
	parts := strings.FieldsFunc(segment, func(r rune) bool {
		return r == '-' || r == '_' || r == '{' || r == '}'
	})
	var words []string
	for _, part := range parts {
		if part != "" {
			words = append(words, part)
		}
	}
	return words
}

func splitIdentifierWords(value string) []string {
	if value == "" {
		return nil
	}
	var words []string
	start := 0
	for index := 1; index < len(value); index++ {
		if value[index] >= 'A' && value[index] <= 'Z' {
			words = append(words, value[start:index])
			start = index
		}
	}
	words = append(words, value[start:])
	return words
}

func exportedWord(word string) string {
	if strings.EqualFold(word, "id") {
		return "ID"
	}
	return strings.ToUpper(word[:1]) + word[1:]
}

func unexportedFirst(value string) string {
	if value == "" {
		return value
	}
	if strings.HasPrefix(value, "ID") {
		return "id" + value[2:]
	}
	return strings.ToLower(value[:1]) + value[1:]
}
