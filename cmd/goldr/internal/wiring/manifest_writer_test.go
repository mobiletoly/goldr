func TestGenerateManifestWritesMetadataAndHandler(t *testing.T) {
	source := generateOK(t, rootManifest())

	for _, want := range []string{
		"type goldrManifest struct",
		"type goldrAction struct",
		"type ErrorHandlers struct",
		"type HandlerOptions struct",
		"TemplateInspection goldr.TemplateInspectionMode",
		"var goldrGeneratedManifest = goldrManifest",
		"func Handler() http.Handler",
		"func HandlerWithOptions(options HandlerOptions) http.Handler",
		"Route:  \"/\"",
		"RoutePrefix: \"/\"",
		"goldr.WritePageRouteResponse(w, r, routeResponse, func(r *http.Request, page goldr.Page) (templ.Component, error)",
	} {
		if !strings.Contains(source, want) {
			t.Fatalf("generated source missing %q:\n%s", want, source)
		}
	}
	if strings.Contains(source, "InspectTemplates") {
		t.Fatalf("generated source contains removed InspectTemplates field:\n%s", source)
	}
}

func TestGenerateManifestWritesRouteSurfaceComment(t *testing.T) {
	source := generateOK(t, routing.Manifest{
		Pages: []routing.ManifestPage{
			{Route: "/", Unit: completeUnit("page.go")},
			{Route: "/users/{id}", Params: []string{"id"}, Unit: completeUnit("users/by_id/page.go")},
		},
		Layouts: []routing.ManifestLayout{
			{RoutePrefix: "/", Unit: completeUnit("layout.go")},
		},
		Fragments: []routing.ManifestFragment{
			{Name: "table", RoutePrefix: "/users", Unit: completeUnit("users/frag_table.go")},
		},
		Actions: []routing.ManifestAction{
			{Method: "POST", Route: "/users/create", GoFile: "users/actions.go", Function: "PostCreate"},
		},
	})

	want := `// Code generated by goldr; DO NOT EDIT.
//
// Route surface:
//   KIND      METHODS   PATH           SOURCE
//   layout    -         /              layout.go
//   page      GET,HEAD  /              page.go
//   action    POST      /users/create  users/actions.go:PostCreate
//   fragment  GET,HEAD  /users/table   users/frag_table.go
//   page      GET,HEAD  /users/{id}    users/by_id/page.go

package routes
`
	if !strings.Contains(source, want) {
		t.Fatalf("generated source missing route surface comment:\n%s", source)
	}
}

func TestGenerateManifestWritesCallSiteComments(t *testing.T) {
	source := trimGeneratedLineIndent(generateOK(t, routing.Manifest{
		Pages: []routing.ManifestPage{
			{Route: "/users", Unit: completeUnit("users/page.go")},
		},
		Layouts: []routing.ManifestLayout{
			{RoutePrefix: "/users", Unit: completeUnit("users/layout.go")},
		},
		Fragments: []routing.ManifestFragment{
			{Name: "table", RoutePrefix: "/users", Unit: completeUnit("users/frag_table.go")},
		},
		Actions: []routing.ManifestAction{
			{Method: "POST", Route: "/users/save-preview", GoFile: "users/actions.go", Function: "PostSavePreview"},
		},
	}))

	for _, want := range []string{
		`// page GET,HEAD /users
// expected in file: app/routes/users/page.go
// expected function: func Page(*http.Request) goldr.RouteResponse { ... }`,
		`// layout /users
// expected in file: app/routes/users/layout.go
// expected function: func Layout(*http.Request, goldr.LayoutContext) templ.Component { ... }
component = goldrroute_users.Layout(r, layoutContext)`,
		`// fragment GET,HEAD /users/table
// expected in file: app/routes/users/frag_table.go
// expected function: func FragTable(*http.Request) goldr.RouteResponse { ... }`,
		`// action POST /users/save-preview
// expected in file: app/routes/users/actions.go
// expected function: func PostSavePreview(*http.Request) goldr.RouteResponse { ... }`,
	} {
		if !strings.Contains(source, want) {
			t.Fatalf("generated source missing call-site comment block:\n--- want ---\n%s\n--- source ---\n%s", want, source)
		}
	}
}

