func TestFullFeatureExampleGeneratedManifestIsCurrent(t *testing.T) {
	source, err := GenerateManifest(fullFeatureManifest(), GenerateOptions{
		PackageName:         "routes",
		RouteRootImportPath: "github.com/mobiletoly/goldr/examples/full_feature/app/routes",
	})
	if err != nil {
		t.Fatalf("GenerateManifest() error = %v, want nil", err)
	}
	path := filepath.Join(goldrRepoRoot(t), "examples", "full_feature", "app", "routes", GeneratedFileName)
	got, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("ReadFile(%q) error = %v", path, err)
	}

	if string(got) != string(source) {
		t.Fatalf("%s is stale\n--- got ---\n%s\n--- want ---\n%s", path, got, source)
	}
}

func TestGenerateManifestReturnsRenderUnitValidationErrors(t *testing.T) {
	manifest := routing.Manifest{
		Layouts: []routing.ManifestLayout{
			{RoutePrefix: "/", Unit: routing.RenderUnit{GoFile: "layout.go"}},
		},
	}

	_, err := GenerateManifest(manifest, GenerateOptions{PackageName: "routes"})
	var validationErr *renderunit.ValidationError
	if !errors.As(err, &validationErr) {
		t.Fatalf("GenerateManifest() error = %T, want *renderunit.ValidationError", err)
	}

	want := renderunit.Problem{Kind: renderunit.KindLayout, Identifier: "/", GoFile: "layout.go", Message: "missing matching .templ file"}
	if len(validationErr.Problems) != 1 || validationErr.Problems[0] != want {
		t.Fatalf("problems = %#v, want %#v", validationErr.Problems, []renderunit.Problem{want})
	}
}

func TestGenerateManifestAcceptsPageWithoutTempl(t *testing.T) {
	manifest := routing.Manifest{
		Pages: []routing.ManifestPage{
			{Route: "/", Unit: routing.RenderUnit{GoFile: "page.go"}},
		},
	}

	source, err := GenerateManifest(manifest, GenerateOptions{
		PackageName:         "routes",
		RouteRootImportPath: "example.com/app/routes",
	})
	if err != nil {
		t.Fatalf("GenerateManifest() error = %v, want nil", err)
	}
	for _, want := range []string{
		`GoFile:    "page.go"`,
		`TemplFile: ""`,
		`source=app/routes/page.go go=app/routes/page.go`,
	} {
		if !strings.Contains(string(source), want) {
			t.Fatalf("generated source missing %q:\n%s", want, source)
		}
	}
}

func TestGenerateManifestRejectsInvalidPackageNames(t *testing.T) {
	tests := []string{"", "App", "app-name", "1app", "func"}

	for _, test := range tests {
		_, err := GenerateManifest(routing.Manifest{}, GenerateOptions{PackageName: test})
		if !errors.Is(err, ErrInvalidPackageName) {
			t.Fatalf("GenerateManifest(..., %q) error = %v, want ErrInvalidPackageName", test, err)
		}
	}
}
