type GeneratedRoutePackageFile struct {
	Dir     string
	Content []byte
}

type routeAdapterImport struct {
	Name     string
	Path     string
	Explicit bool
}

func GenerateRoutePackageFiles(manifest routing.Manifest, options GenerateOptions) ([]GeneratedRoutePackageFile, error) {
	if err := renderunitValidateForRoutePackages(manifest); err != nil {
		return nil, err
	}
	inspectorImportPath := options.InspectorImportPath
	if inspectorImportPath == "" {
		inspectorImportPath = defaultInspectorImportPath(options.RouteRootImportPath)
	}

	fragmentsByDir := nestedFragmentsByDir(fragmentWrappers(manifest))
	routesByDir := nestedRoutesByDir(manifest.Routes)
	if inspectorImportPath == "" && len(fragmentsByDir) > 0 {
		return nil, ErrInvalidRouteRootImportPath
	}

	dirs := routePackageDirs(fragmentsByDir, routesByDir)
	files := make([]GeneratedRoutePackageFile, 0, len(dirs))
	for _, dir := range dirs {
		fragments := fragmentsByDir[dir]
		routes := routesByDir[dir]
		packageSource := packageSourceFile(fragments, routes)
		packageName, err := packageNameForGoFile(manifest.Root, packageSource)
		if err != nil {
			return nil, err
		}

		adapterImports, err := routeAdapterImports(manifest.Root, routes, options.RouteRootImportPath)
		if err != nil {
			return nil, err
		}
		var buffer bytes.Buffer
		buffer.WriteString("// Code generated by goldr; DO NOT EDIT.\n\n")
		fmt.Fprintf(&buffer, "package %s\n\n", packageName)
		writeRoutePackageImports(&buffer, fragments, routes, inspectorImportPath, adapterImports)
		if err := writeRouteDeclarationReference(&buffer, routes, options.RouteRootImportPath); err != nil {
			return nil, err
		}
		if err := writeRouteAdapterFunctions(&buffer, manifest.Root, routes, options.RouteRootImportPath); err != nil {
			return nil, err
		}
		writeFragmentWrapperFunctions(&buffer, fragments)

		source, err := format.Source(buffer.Bytes())
		if err != nil {
			return nil, fmt.Errorf("format generated route package file: %w", err)
		}
		files = append(files, GeneratedRoutePackageFile{Dir: dir, Content: source})
	}
	return files, nil
}

func renderunitValidateForRoutePackages(manifest routing.Manifest) error {
	return renderunit.ValidateManifest(manifest)
}

func nestedFragmentsByDir(fragments []routing.ManifestFragment) map[string][]routing.ManifestFragment {
	fragmentsByDir := make(map[string][]routing.ManifestFragment)
	for _, fragment := range fragments {
		dir := routeSourceDir(fragment.Unit.GoFile)
		if dir == "" {
			continue
		}
		fragmentsByDir[dir] = append(fragmentsByDir[dir], fragment)
	}
	return fragmentsByDir
}

func fragmentWrappers(manifest routing.Manifest) []routing.ManifestFragment {
	fragments := slices.Clone(manifest.Fragments)
	for _, route := range manifest.Routes {
		unit := routing.RenderUnit{GoFile: route.GoFile}
		for _, fragment := range route.Fragments {
			fragments = append(fragments, routing.ManifestFragment{
				Name:        fragment.Name,
				RoutePrefix: route.Route,
				Params:      slices.Clone(route.Params),
				Unit:        unit,
				Function:    routeFragmentAdapterName(route, fragment),
				Segment:     fragment.Segment,
				Index:       fragment.Index,
			})
		}
	}
	return fragments
}

func nestedRoutesByDir(routes []routing.ManifestRouteDeclaration) map[string][]routing.ManifestRouteDeclaration {
	routesByDir := make(map[string][]routing.ManifestRouteDeclaration)
	for _, route := range routes {
		dir := routeSourceDir(route.GoFile)
		if dir == "" {
			continue
		}
		routesByDir[dir] = append(routesByDir[dir], route)
	}
	return routesByDir
}

func routePackageDirs(fragmentsByDir map[string][]routing.ManifestFragment, routesByDir map[string][]routing.ManifestRouteDeclaration) []string {
	seen := make(map[string]bool, len(fragmentsByDir)+len(routesByDir))
	for dir := range fragmentsByDir {
		seen[dir] = true
	}
	for dir := range routesByDir {
		seen[dir] = true
	}
	dirs := make([]string, 0, len(seen))
	for dir := range seen {
		dirs = append(dirs, dir)
	}
	slices.Sort(dirs)
	return dirs
}

func packageSourceFile(fragments []routing.ManifestFragment, routes []routing.ManifestRouteDeclaration) string {
	if len(routes) > 0 {
		return routes[0].GoFile
	}
	return fragments[0].Unit.GoFile
}

func writeRoutePackageImports(buffer *bytes.Buffer, fragments []routing.ManifestFragment, routes []routing.ManifestRouteDeclaration, inspectorImportPath string, adapterImports []routeAdapterImport) {
	needsHTTP := len(routes) > 0
	needsGoldr := routeAdaptersNeedGoldr(routes)
	needsTempl := len(fragments) > 0
	if !needsHTTP && !needsGoldr && !needsTempl && len(adapterImports) == 0 {
		return
	}

	buffer.WriteString("import (\n")
	if needsHTTP {
		buffer.WriteString("\t\"net/http\"\n")
	}
	if needsHTTP && (needsGoldr || needsTempl || len(adapterImports) > 0) {
		buffer.WriteString("\n")
	}
	if needsTempl {
		buffer.WriteString("\t\"github.com/a-h/templ\"\n")
	}
	if needsGoldr {
		buffer.WriteString("\t\"github.com/mobiletoly/goldr\"\n")
	}
	if needsTempl && inspectorImportPath != "" {
		fmt.Fprintf(buffer, "\t%s\n", strconv.Quote(inspectorImportPath))
	}
	for _, item := range adapterImports {
		writeRouteAdapterImport(buffer, item)
	}
	buffer.WriteString(")\n\n")
}

func writeRouteAdapterFunctions(buffer *bytes.Buffer, root string, routes []routing.ManifestRouteDeclaration, routeRootImportPath string) error {
	for _, route := range routes {
		if route.Page != nil {
			fmt.Fprintf(buffer, "func %s(r *http.Request) goldr.PageRouteResponse {\n", routePageAdapterName(route))
			if err := writeRouteAdapterCall(buffer, root, routeRootImportPath, route, route.Page.Handler, "r", true); err != nil {
				return err
			}
			buffer.WriteString("}\n\n")
		}
		for _, fragment := range route.Fragments {
			fmt.Fprintf(buffer, "func %s(r *http.Request) goldr.FragmentRouteResponse {\n", routeFragmentAdapterName(route, fragment))
			if err := writeRouteAdapterCall(buffer, root, routeRootImportPath, route, fragment.Handler, "r", true); err != nil {
				return err
			}
			buffer.WriteString("}\n\n")
		}
		for _, action := range route.Actions {
			if action.Writer {
				if route.Kit != nil {
					fmt.Fprintf(buffer, "func %s(w http.ResponseWriter, r *http.Request) error {\n", routeActionAdapterName(route, action))
					if err := writeKitWriterRouteAdapterCall(buffer, root, routeRootImportPath, route, action.Handler); err != nil {
						return err
					}
				} else {
					fmt.Fprintf(buffer, "func %s(w http.ResponseWriter, r *http.Request) {\n", routeActionAdapterName(route, action))
					if err := writeRouteAdapterCall(buffer, root, routeRootImportPath, route, action.Handler, "w, r", false); err != nil {
						return err
					}
				}
			} else {
				fmt.Fprintf(buffer, "func %s(r *http.Request) goldr.RouteResponse {\n", routeActionAdapterName(route, action))
				if err := writeRouteAdapterCall(buffer, root, routeRootImportPath, route, action.Handler, "r", true); err != nil {
					return err
				}
			}
			buffer.WriteString("}\n\n")
		}
	}
	return nil
}

func writeRouteDeclarationReference(buffer *bytes.Buffer, routes []routing.ManifestRouteDeclaration, routeRootImportPath string) error {
	if len(routes) == 0 {
		return nil
	}
	buffer.WriteString("// Route is read by goldr tooling; this reference keeps editors from marking it unused.\n")
	buffer.WriteString("var _ = Route\n\n")
	for _, route := range mountedRouteReferences(routes) {
		item, err := routeAdapterMountImport(route, routeRootImportPath)
		if err != nil {
			return err
		}
		fmt.Fprintf(buffer, "var _ = %s.Route\n\n", item.Name)
	}
	return nil
}

func writeRouteAdapterCall(buffer *bytes.Buffer, root string, routeRootImportPath string, route routing.ManifestRouteDeclaration, handler string, args string, returns bool) error {
	callHandler, err := routeAdapterCallExpression(root, routeRootImportPath, route, handler)
	if err != nil {
		return err
	}
	if route.Kit == nil {
		if returns {
			fmt.Fprintf(buffer, "\treturn %s(%s)\n", callHandler, args)
			return nil
		}
		fmt.Fprintf(buffer, "\t%s(%s)\n", callHandler, args)
		return nil
	}

	fmt.Fprintf(buffer, "\tgoldrKit, err := %s(r)\n", route.Kit.New)
	buffer.WriteString("\tif err != nil {\n")
	buffer.WriteString("\t\treturn goldr.RouteError{Err: err}\n")
	buffer.WriteString("\t}\n")
	if returns {
		fmt.Fprintf(buffer, "\treturn %s(goldrKit, %s)\n", callHandler, args)
		return nil
	}
	fmt.Fprintf(buffer, "\t%s(goldrKit, %s)\n", callHandler, args)
	return nil
}

func writeKitWriterRouteAdapterCall(buffer *bytes.Buffer, root string, routeRootImportPath string, route routing.ManifestRouteDeclaration, handler string) error {
	callHandler, err := routeAdapterCallExpression(root, routeRootImportPath, route, handler)
	if err != nil {
		return err
	}
	fmt.Fprintf(buffer, "\tgoldrKit, err := %s(r)\n", route.Kit.New)
	buffer.WriteString("\tif err != nil {\n")
	buffer.WriteString("\t\treturn err\n")
	buffer.WriteString("\t}\n")
	fmt.Fprintf(buffer, "\t%s(goldrKit, w, r)\n", callHandler)
	buffer.WriteString("\treturn nil\n")
	return nil
}

func routeAdapterImports(root string, routes []routing.ManifestRouteDeclaration, routeRootImportPath string) ([]routeAdapterImport, error) {
	seen := make(map[string]routeAdapterImport)
	for _, route := range routes {
		imports := make(map[string]routing.RouteImportDeclaration, len(route.Imports))
		for _, item := range route.Imports {
			if item.Name == "_" {
				return nil, errors.New("route.go must not use blank imports")
			}
			if item.Name == "." {
				return nil, errors.New("route.go must not use dot imports")
			}
			if existing, ok := imports[item.Name]; ok && existing.Path != item.Path {
				return nil, fmt.Errorf("route adapter imports for %s use alias %q for both %q and %q", route.GoFile, item.Name, existing.Path, item.Path)
			}
			imports[item.Name] = item
		}
		if route.Source != "" {
			item, err := routeAdapterMountImport(route, routeRootImportPath)
			if err != nil {
				return nil, err
			}
			key := item.Name + "\x00" + item.Path
			seen[key] = item
		}
		localNames, err := routePackageLocalNames(root, route.GoFile)
		if err != nil {
			return nil, err
		}
		mountLocalNames, err := routePackageLocalNames(root, route.Source)
		if err != nil {
			return nil, err
		}
		for _, expr := range routeAdapterExpressions(route) {
			names, err := routeAdapterExpressionPrefixes(expr)
			if err != nil {
				return nil, fmt.Errorf("parse route adapter expression %q: %w", expr, err)
			}
			for _, name := range names {
				item, ok := imports[name]
				if !ok {
					if root == "" || localNames[name] {
						if !mountLocalNames[name] {
							continue
						}
					}
					if mountLocalNames[name] {
						item, err := routeAdapterMountImport(route, routeRootImportPath)
						if err != nil {
							return nil, err
						}
						key := item.Name + "\x00" + item.Path
						seen[key] = item
						continue
					}
					return nil, fmt.Errorf("route adapter expression %q in %s references %q, which is not an import name or route-package declaration; add an explicit import alias in route.go when an imported package name differs from its path base", expr, route.GoFile, name)
				}
				if item.Path == "github.com/mobiletoly/goldr" || item.Path == "net/http" {
					continue
				}
				key := item.Name + "\x00" + item.Path
				seen[key] = routeAdapterImport{Name: item.Name, Path: item.Path, Explicit: item.Explicit}
			}
		}
	}

	result := make([]routeAdapterImport, 0, len(seen))
	for _, item := range seen {
		result = append(result, item)
	}
	slices.SortFunc(result, func(a, b routeAdapterImport) int {
		if a.Path != b.Path {
			return strings.Compare(a.Path, b.Path)
		}
		return strings.Compare(a.Name, b.Name)
	})
	return result, nil
}

func mountedRouteReferences(routes []routing.ManifestRouteDeclaration) []routing.ManifestRouteDeclaration {
	seen := make(map[string]bool)
	result := make([]routing.ManifestRouteDeclaration, 0)
	for _, route := range routes {
		if route.Source == "" {
			continue
		}
		if seen[route.Source] {
			continue
		}
		seen[route.Source] = true
		result = append(result, route)
	}
	return result
}

func routeAdapterMountImport(route routing.ManifestRouteDeclaration, routeRootImportPath string) (routeAdapterImport, error) {
	importPath, err := routeAdapterMountImportPath(route, routeRootImportPath)
	if err != nil {
		return routeAdapterImport{}, err
	}
	for _, item := range route.Imports {
		if item.Path == importPath {
			return routeAdapterImport{Name: item.Name, Path: item.Path, Explicit: item.Explicit}, nil
		}
	}
	dir := routeSourceDir(route.Source)
	return routeAdapterImport{
		Name:     routeImportAlias(dir),
		Path:     importPath,
		Explicit: true,
	}, nil
}

func routeAdapterMountImportPath(route routing.ManifestRouteDeclaration, routeRootImportPath string) (string, error) {
	if routeRootImportPath == "" {
		return "", ErrInvalidRouteRootImportPath
	}
	return routeImportPath(routeSourceDir(route.Source), routeRootImportPath), nil
}

func routePackageLocalNames(root string, goFile string) (map[string]bool, error) {
	names := make(map[string]bool)
	if root == "" || goFile == "" {
		return names, nil
	}

	dir := filepath.Join(root, filepath.FromSlash(routeSourceDir(goFile)))
	entries, err := os.ReadDir(dir)
	if err != nil {
		return nil, fmt.Errorf("read route package directory for %s: %w", goFile, err)
	}
	for _, entry := range entries {
		if entry.IsDir() || !isRoutePackageSourceFile(entry.Name()) {
			continue
		}
		path := filepath.Join(dir, entry.Name())
		file, err := parser.ParseFile(token.NewFileSet(), path, nil, parser.SkipObjectResolution)
		if err != nil {
			return nil, fmt.Errorf("parse route package source %s: %w", path, err)
		}
		for _, decl := range file.Decls {
			recordTopLevelDeclarationNames(names, decl)
		}
	}
	return names, nil
}

func routeAdapterCallExpression(root string, routeRootImportPath string, route routing.ManifestRouteDeclaration, expr string) (string, error) {
	if root == "" || route.Source == "" {
		return expr, nil
	}
	first, err := routeAdapterExpressionFirstIdent(expr)
	if err != nil {
		return "", fmt.Errorf("parse route adapter expression %q: %w", expr, err)
	}
	if first == "" {
		return expr, nil
	}
	mountLocalNames, err := routePackageLocalNames(root, route.Source)
	if err != nil {
		return "", err
	}
	if mountLocalNames[first] {
		item, err := routeAdapterMountImport(route, routeRootImportPath)
		if err != nil {
			return "", err
		}
		return item.Name + "." + expr, nil
	}
	localNames, err := routePackageLocalNames(root, route.GoFile)
	if err != nil {
		return "", err
	}
	if localNames[first] {
		return expr, nil
	}
	return expr, nil
}

func routeAdapterExpressionFirstIdent(source string) (string, error) {
	expr, err := parser.ParseExpr(source)
	if err != nil {
		return "", err
	}
	name, _ := leftmostIdent(expr)
	return name, nil
}

func isRoutePackageSourceFile(name string) bool {
	return strings.HasSuffix(name, ".go") &&
		!strings.HasSuffix(name, "_test.go") &&
		!strings.HasSuffix(name, "_templ.go") &&
		name != GeneratedFileName
}

func recordTopLevelDeclarationNames(names map[string]bool, decl ast.Decl) {
	switch decl := decl.(type) {
	case *ast.FuncDecl:
		names[decl.Name.Name] = true
	case *ast.GenDecl:
		for _, spec := range decl.Specs {
			switch spec := spec.(type) {
			case *ast.TypeSpec:
				names[spec.Name.Name] = true
			case *ast.ValueSpec:
				for _, name := range spec.Names {
					names[name.Name] = true
				}
			}
		}
	}
}

func routeAdapterExpressions(route routing.ManifestRouteDeclaration) []string {
	var expressions []string
	if route.Kit != nil {
		expressions = append(expressions, route.Kit.New)
	}
	if route.Page != nil {
		expressions = append(expressions, route.Page.Handler)
	}
	for _, fragment := range route.Fragments {
		expressions = append(expressions, fragment.Handler)
	}
	for _, action := range route.Actions {
		expressions = append(expressions, action.Handler)
	}
	return expressions
}

func routeAdapterExpressionPrefixes(source string) ([]string, error) {
	expr, err := parser.ParseExpr(source)
	if err != nil {
		return nil, err
	}
	seen := map[string]bool{}
	if name, ok := leftmostIdent(expr); ok {
		seen[name] = true
	}
	ast.Inspect(expr, func(node ast.Node) bool {
		selector, ok := node.(*ast.SelectorExpr)
		if !ok {
			return true
		}
		if name, ok := leftmostIdent(selector); ok {
			seen[name] = true
		}
		return true
	})
	names := make([]string, 0, len(seen))
	for name := range seen {
		names = append(names, name)
	}
	slices.Sort(names)
	return names, nil
}

func leftmostIdent(expr ast.Expr) (string, bool) {
	switch expr := expr.(type) {
	case *ast.Ident:
		return expr.Name, true
	case *ast.SelectorExpr:
		return leftmostIdent(expr.X)
	case *ast.IndexExpr:
		return leftmostIdent(expr.X)
	case *ast.IndexListExpr:
		return leftmostIdent(expr.X)
	default:
		return "", false
	}
}

func routeAdaptersNeedGoldr(routes []routing.ManifestRouteDeclaration) bool {
	for _, route := range routes {
		if route.Page != nil || len(route.Fragments) > 0 {
			return true
		}
		for _, action := range route.Actions {
			if !action.Writer {
				return true
			}
		}
	}
	return false
}

func writeRouteAdapterImport(buffer *bytes.Buffer, item routeAdapterImport) {
	if item.Explicit {
		fmt.Fprintf(buffer, "\t%s %s\n", item.Name, strconv.Quote(item.Path))
		return
	}
	fmt.Fprintf(buffer, "\t%s\n", strconv.Quote(item.Path))
}

func rootRouteDeclarations(routes []routing.ManifestRouteDeclaration) []routing.ManifestRouteDeclaration {
	root := make([]routing.ManifestRouteDeclaration, 0)
	for _, route := range routes {
		if routeSourceDir(route.GoFile) == "" {
			root = append(root, route)
		}
	}
	return root
}

func routePageAdapterName(route routing.ManifestRouteDeclaration) string {
	if route.Adapter != "" {
		return "GoldrRoute" + route.Adapter + "Page"
	}
	return "GoldrRoutePage"
}

func routeFragmentAdapterName(route routing.ManifestRouteDeclaration, fragment routing.RouteFragmentDeclaration) string {
	prefix := "GoldrRoute"
	if route.Adapter != "" {
		prefix += route.Adapter
	}
	return prefix + "Frag" + fragment.SymbolName
}

func routeActionAdapterName(route routing.ManifestRouteDeclaration, action routing.RouteActionDeclaration) string {
	prefix := "GoldrRoute"
	if route.Adapter != "" {
		prefix += route.Adapter
	}
	return prefix + routeActionMethodName(action.Method) + action.SymbolName
}

func routeActionMethodName(method string) string {
	switch method {
	case "POST":
		return "Post"
	case "PUT":
		return "Put"
	case "PATCH":
		return "Patch"
	case "DELETE":
		return "Delete"
	default:
		return exportedSegmentName(strings.ToLower(method))
	}
}
