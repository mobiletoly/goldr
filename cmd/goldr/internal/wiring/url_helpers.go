func urlConstructorCallExpression(node *urlHelperNode, basePathExpression string, constructorName func(*urlHelperNode) string) string {
	if urlAliasablePathNode(node) {
		return node.typeName + "(" + urlPathCarrierLiteral(node, urlConstructorPathExpression(node, basePathExpression)) + ")"
	}

	var buffer strings.Builder
	buffer.WriteString(constructorName(node))
	buffer.WriteString("(")
	buffer.WriteString(basePathExpression)
	if len(node.params) > 0 {
		buffer.WriteString(", ")
	}
	for index, param := range node.params {
		if index > 0 {
			buffer.WriteString(", ")
		}
		buffer.WriteString(param.field)
	}
	buffer.WriteString(")")
	return buffer.String()
}

func urlDynamicNodeConstructorCallExpression(node *urlHelperNode, basePathExpression string) string {
	var buffer strings.Builder
	buffer.WriteString(urlDynamicNodeConstructorName(node))
	buffer.WriteString("(")
	buffer.WriteString(basePathExpression)
	parentParams := node.params[:len(node.params)-1]
	if len(parentParams) > 0 {
		buffer.WriteString(", ")
	}
	for index, param := range parentParams {
		if index > 0 {
			buffer.WriteString(", ")
		}
		buffer.WriteString(param.field)
	}
	buffer.WriteString(")")
	return buffer.String()
}

func writeURLDynamicConstructorArgs(buffer *bytes.Buffer, parent, child *urlHelperNode, escapedName string, basePathExpression string) {
	parentParams := make(map[string]bool, len(parent.params))
	for _, param := range parent.params {
		parentParams[param.field] = true
	}
	buffer.WriteString(basePathExpression)
	if len(child.params) > 0 {
		buffer.WriteString(", ")
	}
	for index, param := range child.params {
		if index > 0 {
			buffer.WriteString(", ")
		}
		if parentParams[param.field] {
			buffer.WriteString("r.")
		} else if param.field == child.param.field {
			buffer.WriteString(escapedName)
			continue
		}
		buffer.WriteString(param.field)
	}
}

