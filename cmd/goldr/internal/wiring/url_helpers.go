func collectURLHelperNodes(root *urlHelperNode, includeRoot bool) []*urlHelperNode {
	var nodes []*urlHelperNode
	if includeRoot {
		nodes = append(nodes, root)
	}
	for _, child := range root.staticChildren {
		nodes = append(nodes, collectURLHelperNodes(child, true)...)
	}
	for _, child := range root.dynamicChildren {
		nodes = append(nodes, collectURLHelperNodes(child, true)...)
	}
	return nodes
}

func urlRouteRefNodes(root *urlHelperNode) []*urlHelperNode {
	seen := make(map[*urlHelperNode]bool)
	var result []*urlHelperNode
	var collectDescendants func(*urlHelperNode)
	collectDescendants = func(node *urlHelperNode) {
		for _, child := range node.staticChildren {
			if !seen[child] {
				seen[child] = true
				result = append(result, child)
			}
			collectDescendants(child)
		}
		for _, child := range node.dynamicChildren {
			if !seen[child] {
				seen[child] = true
				result = append(result, child)
			}
			collectDescendants(child)
		}
	}
	for _, node := range collectURLHelperNodes(root, true) {
		for _, child := range node.dynamicChildren {
			collectDescendants(child)
		}
	}
	return result
}

