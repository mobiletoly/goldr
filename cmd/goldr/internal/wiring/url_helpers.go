func writeURLDestinationMethods(buffer *bytes.Buffer, root *urlHelperNode) {
	for _, node := range collectURLHelperNodes(root, true) {
		for _, destination := range node.destinations {
			for index, paramName := range destination.paramNames {
				receiver := urlDestinationStageTypeName(destination, index)
				next := urlDestinationStageTypeName(destination, index+1)
				argName := unexportedIdentifier(paramName)
				escapedName := "escaped" + exportedSegmentName(paramName)
				fmt.Fprintf(buffer, "func (d %s) Bind(%s string) %s {\n", receiver, argName, next)
				fmt.Fprintf(buffer, "\t%s := url.PathEscape(%s)\n", escapedName, argName)
				fmt.Fprintf(buffer, "\treturn %s{\n", next)
				buffer.WriteString("\t\tbasePath: d.basePath,\n")
				for priorIndex := range index {
					priorName := unexportedIdentifier(destination.paramNames[priorIndex])
					fmt.Fprintf(buffer, "\t\t%s: d.%s,\n", priorName, priorName)
				}
				fmt.Fprintf(buffer, "\t\t%s: %s,\n", argName, escapedName)
				buffer.WriteString("\t}\n")
				buffer.WriteString("}\n\n")
			}

			finalType := urlDestinationStageTypeName(destination, len(destination.paramNames))
			fmt.Fprintf(buffer, "func (d %s) Href() string {\n", finalType)
			fmt.Fprintf(buffer, "\tpath := %s\n", urlDestinationPathExpression(destination))
			fmt.Fprintf(buffer, "\treturn goldrURLWithTrail(path, %s)\n", strconv.Quote(destination.trailKey))
			buffer.WriteString("}\n\n")
			if destination.trailKey != "" {
				fmt.Fprintf(buffer, "func (d %s) NavigationHref(nav goldr.Navigation) string {\n", finalType)
				fmt.Fprintf(buffer, "\tpath := %s\n", urlDestinationPathExpression(destination))
				fmt.Fprintf(buffer, "\treturn goldr.NavigationHref(path, %s, nav)\n", strconv.Quote(destination.trailKey))
				buffer.WriteString("}\n\n")
			}
		}
	}
	if urlHelperTreeHasDestinations(root) {
		writeURLDestinationHelpers(buffer)
	}
}

