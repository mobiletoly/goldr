func urlDestinationPathExpression(destination urlHelperDestination) string {
	var buffer strings.Builder
	buffer.WriteString("d.basePath")
	paramFields := make(map[string]string, len(destination.paramNames))
	for _, paramName := range destination.paramNames {
		paramFields[paramName] = unexportedIdentifier(paramName)
	}
	for _, segment := range destination.target.segments {
		buffer.WriteString(" + ")
		buffer.WriteString(strconv.Quote("/"))
		if paramName, ok := paramSegmentName(segment); ok {
			buffer.WriteString(" + d.")
			buffer.WriteString(paramFields[paramName])
			continue
		}
		buffer.WriteString(" + ")
		buffer.WriteString(strconv.Quote(segment))
	}
	if len(destination.target.segments) == 0 {
		buffer.WriteString(" + ")
		buffer.WriteString(strconv.Quote("/"))
	}
	return buffer.String()
}

func urlRoutePattern(node *urlHelperNode) string {
	if len(node.segments) == 0 {
		return "/"
	}
	return "/" + strings.Join(node.segments, "/")
}

