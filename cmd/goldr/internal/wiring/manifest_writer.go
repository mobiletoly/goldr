type routeSurfaceCommentRow struct {
	kind    string
	methods string
	path    string
	source  string
}

func writeGeneratedFileHeader(buffer *bytes.Buffer, rows []RouteSurfaceRow) {
	buffer.WriteString("// Code generated by goldr; DO NOT EDIT.\n")
	buffer.WriteString("//\n")
	buffer.WriteString("// Route surface:\n")

	commentRows := make([]routeSurfaceCommentRow, 0, len(rows)+1)
	commentRows = append(commentRows, routeSurfaceCommentRow{
		kind:    "KIND",
		methods: "METHODS",
		path:    "PATH",
		source:  "SOURCE",
	})
	for _, row := range rows {
		commentRows = append(commentRows, routeSurfaceCommentRow{
			kind:    row.Kind,
			methods: FormatRouteSurfaceList(row.Methods),
			path:    row.Path,
			source:  row.Source,
		})
	}

	kindWidth := routeSurfaceCommentWidth(commentRows, func(row routeSurfaceCommentRow) string { return row.kind })
	methodsWidth := routeSurfaceCommentWidth(commentRows, func(row routeSurfaceCommentRow) string { return row.methods })
	pathWidth := routeSurfaceCommentWidth(commentRows, func(row routeSurfaceCommentRow) string { return row.path })

	for _, row := range commentRows {
		fmt.Fprintf(
			buffer,
			"//   %-*s  %-*s  %-*s  %s\n",
			kindWidth,
			row.kind,
			methodsWidth,
			row.methods,
			pathWidth,
			row.path,
			row.source,
		)
	}
	buffer.WriteString("\n")
}

func routeSurfaceCommentWidth(rows []routeSurfaceCommentRow, value func(routeSurfaceCommentRow) string) int {
	width := 0
	for _, row := range rows {
		width = max(width, len(value(row)))
	}
	return width
}

func writeImports(buffer *bytes.Buffer, imports []routeImport, adapterImports []routeAdapterImport, inspectorImportPath string, needsDynamicRoutes, needsRuntime, needsRouteRenderer, needsSegments, needsGoldr bool) {
	if !needsRuntime {
		return
	}

	buffer.WriteString("import (\n")
	buffer.WriteString("\t\"net/http\"\n")
	if needsDynamicRoutes {
		buffer.WriteString("\t\"net/url\"\n")
	}
	if needsSegments {
		buffer.WriteString("\t\"strings\"\n")
	}
	buffer.WriteString("\n")
	if needsRouteRenderer {
		buffer.WriteString("\t\"github.com/a-h/templ\"\n")
	}
	if needsGoldr {
		buffer.WriteString("\t\"github.com/mobiletoly/goldr\"\n")
	}
	if inspectorImportPath != "" {
		fmt.Fprintf(buffer, "\t%s\n", strconv.Quote(inspectorImportPath))
	}
	for _, item := range adapterImports {
		writeRouteAdapterImport(buffer, item)
	}
	for _, item := range imports {
		fmt.Fprintf(buffer, "\t%s %s\n", item.Alias, strconv.Quote(item.ImportPath))
	}
	buffer.WriteString(")\n\n")
}

func writeTypes(buffer *bytes.Buffer, needsRuntime bool) {
	buffer.WriteString(`type goldrManifest struct {
	Pages       []goldrPage
	Layouts     []goldrLayout
	Fragments   []goldrFragment
	Actions     []goldrAction
	Middlewares []goldrMiddleware
}

type goldrPage struct {
	Route  string
	Params []string
	Unit   goldrRenderUnit
}

type goldrLayout struct {
	RoutePrefix string
	Params      []string
	Unit        goldrRenderUnit
}

type goldrFragment struct {
	Name        string
	RoutePrefix string
	Params      []string
	Unit        goldrRenderUnit
	Index       bool
}

type goldrAction struct {
	Method       string
	Route        string
	Params       []string
	GoFile       string
	SourceGoFile string
	Function     string
	Suffix       string
	Segment      string
}

type goldrMiddleware struct {
	RoutePrefix string
	Params      []string
	GoFile      string
}

type goldrRenderUnit struct {
	GoFile       string
	SourceGoFile string
	TemplFile    string
}

`)
	if needsRuntime {
		buffer.WriteString(`type ErrorHandlers struct {
	RouteNotFound         func(*http.Request) goldr.RouteResponse
	RouteMethodNotAllowed func(*http.Request) goldr.RouteResponse
	RouteError            func(*http.Request, error) goldr.RouteResponse
}

type HandlerOptions struct {
	ErrorHandlers       ErrorHandlers
	TemplateInspection goldr.TemplateInspectionMode
}

`)
	}
}

func writeManifestValue(buffer *bytes.Buffer, manifest routing.Manifest) {
	buffer.WriteString("var goldrGeneratedManifest = goldrManifest{\n")
	buffer.WriteString("\tPages: []goldrPage{\n")
	for _, page := range manifest.Pages {
		buffer.WriteString("\t\t{\n")
		fmt.Fprintf(buffer, "\t\t\tRoute: %s,\n", strconv.Quote(page.Route))
		writeParams(buffer, page.Params)
		writeRenderUnit(buffer, page.Unit)
		buffer.WriteString("\t\t},\n")
	}
	buffer.WriteString("\t},\n")

	buffer.WriteString("\tLayouts: []goldrLayout{\n")
	for _, layout := range manifest.Layouts {
		buffer.WriteString("\t\t{\n")
		fmt.Fprintf(buffer, "\t\t\tRoutePrefix: %s,\n", strconv.Quote(layout.RoutePrefix))
		writeParams(buffer, layout.Params)
		writeRenderUnit(buffer, layout.Unit)
		buffer.WriteString("\t\t},\n")
	}
	buffer.WriteString("\t},\n")

	buffer.WriteString("\tFragments: []goldrFragment{\n")
	for _, fragment := range manifest.Fragments {
		buffer.WriteString("\t\t{\n")
		fmt.Fprintf(buffer, "\t\t\tName: %s,\n", strconv.Quote(fragment.Name))
		fmt.Fprintf(buffer, "\t\t\tRoutePrefix: %s,\n", strconv.Quote(fragment.RoutePrefix))
		writeParams(buffer, fragment.Params)
		writeRenderUnit(buffer, fragment.Unit)
		if fragment.Index {
			buffer.WriteString("\t\t\tIndex: true,\n")
		}
		buffer.WriteString("\t\t},\n")
	}
	buffer.WriteString("\t},\n")

	buffer.WriteString("\tActions: []goldrAction{\n")
	for _, action := range manifest.Actions {
		buffer.WriteString("\t\t{\n")
		fmt.Fprintf(buffer, "\t\t\tMethod: %s,\n", strconv.Quote(action.Method))
		fmt.Fprintf(buffer, "\t\t\tRoute: %s,\n", strconv.Quote(action.Route))
		writeParams(buffer, action.Params)
		fmt.Fprintf(buffer, "\t\t\tGoFile: %s,\n", strconv.Quote(action.GoFile))
		if action.SourceGoFile != "" {
			fmt.Fprintf(buffer, "\t\t\tSourceGoFile: %s,\n", strconv.Quote(action.SourceGoFile))
		}
		fmt.Fprintf(buffer, "\t\t\tFunction: %s,\n", strconv.Quote(action.Function))
		fmt.Fprintf(buffer, "\t\t\tSuffix: %s,\n", strconv.Quote(action.Suffix))
		fmt.Fprintf(buffer, "\t\t\tSegment: %s,\n", strconv.Quote(action.Segment))
		buffer.WriteString("\t\t},\n")
	}
	buffer.WriteString("\t},\n")
	buffer.WriteString("\tMiddlewares: []goldrMiddleware{\n")
	for _, middleware := range manifest.Middlewares {
		buffer.WriteString("\t\t{\n")
		fmt.Fprintf(buffer, "\t\t\tRoutePrefix: %s,\n", strconv.Quote(middleware.RoutePrefix))
		writeParams(buffer, middleware.Params)
		fmt.Fprintf(buffer, "\t\t\tGoFile: %s,\n", strconv.Quote(middleware.GoFile))
		buffer.WriteString("\t\t},\n")
	}
	buffer.WriteString("\t},\n")
	buffer.WriteString("}\n")
}

func writeParams(buffer *bytes.Buffer, params []string) {
	buffer.WriteString("\t\t\tParams: ")
	if len(params) == 0 {
		buffer.WriteString("nil,\n")
		return
	}

	buffer.WriteString("[]string{")
	for index, param := range params {
		if index > 0 {
			buffer.WriteString(", ")
		}
		buffer.WriteString(strconv.Quote(param))
	}
	buffer.WriteString("},\n")
}

func writeRenderUnit(buffer *bytes.Buffer, unit routing.RenderUnit) {
	buffer.WriteString("\t\t\tUnit: goldrRenderUnit{\n")
	fmt.Fprintf(buffer, "\t\t\t\tGoFile: %s,\n", strconv.Quote(unit.GoFile))
	if unit.SourceGoFile != "" {
		fmt.Fprintf(buffer, "\t\t\t\tSourceGoFile: %s,\n", strconv.Quote(unit.SourceGoFile))
	}
	fmt.Fprintf(buffer, "\t\t\t\tTemplFile: %s,\n", strconv.Quote(unit.TemplFile))
	buffer.WriteString("\t\t\t},\n")
}
